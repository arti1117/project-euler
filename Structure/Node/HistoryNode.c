typedef struct char_node
{
    char data;
    char root;
} cnode;

typedef struct int_node
{
    int data;
    int root;
} inode;

typedef struct float_node
{
    float data;
    float root;
} fnode;

typedef struct double_node
{
    double data;
    double root;
} dnode;

typedef struct string_node
{
    char* data;
    char* root;
} snode;
